// 検索用: 全記事をフラット化
export function getAllArticles() {
  return HELP_CATEGORIES.flatMap(c => c.articles.map(a => ({ ...a, category: c.name, categoryId: c.id, categoryIcon: c.icon })));
}

// IDで検索
export function getArticle(articleId) {
  for (const c of HELP_CATEGORIES) {
    const a = c.articles.find(a => a.id === articleId);
    if (a) return { ...a, category: c.name, categoryId: c.id };
  }
  return null;
}
